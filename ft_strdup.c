#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*cpy;
	size_t	size;
	size_t	i;

	if (!s1)
		return (NULL);
	size = ft_strlen((char *)s1);
	cpy = (char *)malloc(sizeof(char) * (size + 1));
	if (!cpy)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		cpy[i] = s1[i];
		i++;
	}
	cpy[i] = 0;
	return (cpy);
}
